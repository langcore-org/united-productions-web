   - 推奨理由（なぜこの企画に適しているか）

2. **出演者の相性分析**
   - 候補同士の相性
   - 想定される化学反応

3. **注意事項・リスク**
   - スケジュール上の制約（一般的な傾向）
   - イメージ上の注意点

### 制約
- 具体的な連絡先や個人情報は含めない
- 公開情報に基づいた分析を行う
- 中立的な立場で複数の候補を提示する',
