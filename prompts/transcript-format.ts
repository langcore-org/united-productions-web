/**
 * Transcript Format Prompts
 * 
 * Premiere Pro書き起こしテキストをNA原稿用に整形するプロンプト
 */

/**
 * 話者ラベルの型
 */
export type SpeakerLabel = '櫻井' | '末澤' | 'N' | 'その他';

/**
 * 話者情報インターフェース
 */
export interface Speaker {
  label: SpeakerLabel;
  description?: string;
}

/**
 * システムプロンプトを取得
 * @returns システムプロンプト
 */
export function getTranscriptSystemPrompt(): string {
  return `あなたはテレビ制作のNA原稿作成の専門家です。
Premiere Proから書き出された文字起こしテキストを、NA原稿用に整形してください。

## 整形ルール

### 話者ラベル
以下の話者ラベルを使用してください：
- **櫻井** - 櫻井翔
- **末澤** - 末澤誠也
- **N** - ナレーション
- **その他** - その他の出演者・スタッフ

### 整形要件
1. **話者判定**: コンテキストから話者を推定し、適切なラベルを付ける
2. **不要な要素の削除**: 
   - タイムコード（00:00:00など）
   - 話者ID（Speaker 1など）
   - フィラー（「えー」「あー」など）
   - 重複した言い直し
3. **文の整形**:
   - 適切な句読点を追加
   - 段落分けを整理
   - 同じ話者の連続した発言はまとめる
4. **出力形式**:
   - Markdown形式
   - 話者ラベルは太字（**櫻井**:）
   - 発言内容は通常テキスト

### 出力例
\`\`\`markdown
**櫻井**: 今日はよろしくお願いします。

**末澤**: こちらこそ、よろしくお願いします。

**N**: 二人の出会いは、ある番組の収録現場でした。

**櫻井**: 初めてお会いした時、すごい緊張しましたね。

**末澤**: そうですね。でも櫻井さんが話しかけてくださって。
\`\`\`

### 注意事項
- 話者が不明確な場合は「**その他**」を使用
- ナレーションは明確に「**N**」として区別
- 自然な会話の流れを保ちながら整形
- 意味のある内容のみ残し、ノイズは除去`;
}

/**
 * ユーザープロンプトを生成
 * @param transcript - 整形前のテキスト
 * @returns ユーザープロンプト
 */
export function createUserPrompt(transcript: string): string {
  return `以下のPremiere Pro書き起こしテキストをNA原稿用に整形してください：

---

${transcript}

---

整形したテキストのみを出力してください。説明や注釈は不要です。`;
}
